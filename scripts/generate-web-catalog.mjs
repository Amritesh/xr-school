#!/usr/bin/env node
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { parseCatalogCsv } from './validate-simulation-catalog.mjs';

const ROOT = process.cwd();
const CATALOG_PATH = resolve(ROOT, 'docs/catalog/class-5-to-10-science-virtual-tours-catalog.csv');
const OUTPUT_PATH = resolve(ROOT, 'apps/web/lib/scienceCatalog.generated.ts');

function toNumber(value) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : 0;
}

export function toWebCatalogRows(rows) {
  return rows.map(row => ({
    id: row.simulationId,
    slug: row.slug,
    classLevel: toNumber(row.class),
    gradeBand: row.gradeBand,
    chapter: toNumber(row.chapter),
    topic: row.topic,
    activityNumber: toNumber(row.activityNumber),
    title: row.activityTitle,
    subject: row.subject,
    primaryArchetype: row.primaryArchetype,
    secondaryArchetypes: row.secondaryArchetypes ? row.secondaryArchetypes.split('|').filter(Boolean) : [],
    simulationFormat: row.simulationFormat,
    xrFitType: row.xrFitType,
    comfortRiskLevel: row.comfortRiskLevel,
    expectedDurationMinutes: toNumber(row.expectedDurationMinutes),
    reuseGroup: row.reuseGroup,
    implementationNotes: row.implementationNotes,
    releaseMaturity: 'catalogued',
  }));
}

export function renderWebCatalogSource(rows) {
  return `// Generated by scripts/generate-web-catalog.mjs. Do not edit manually.
export const SCIENCE_SIMULATION_CATALOG = ${JSON.stringify(rows, null, 2)} as const;

export type ScienceSimulationCatalogItem = (typeof SCIENCE_SIMULATION_CATALOG)[number];
export type ScienceSimulationArchetype = ScienceSimulationCatalogItem['primaryArchetype'];
`;
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const rows = toWebCatalogRows(parseCatalogCsv(readFileSync(CATALOG_PATH, 'utf8')));
  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, renderWebCatalogSource(rows));
  console.log(`Generated ${rows.length} web catalog rows at ${OUTPUT_PATH}`);
}
